-- 3. REMOVE TEST DATA
DELETE FROM FOODITEMLIST
WHERE MENUID = 1001 AND FOODITEMID = 1002;

-- 4. AFTER - AUDIT TABLE
COLUMN TABLE_NAME FORMAT A25
COLUMN BY_USER FORMAT A7
SELECT * FROM AUDITS_MENU;

-- 5. DELETE ALL DATA
TRUNCATE TABLE AUDITS_MENU;